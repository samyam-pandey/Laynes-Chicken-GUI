    private void add_menu_item() {
        try {
            Connection conn = null;
            Class.forName("org.postgresql.Driver");
            conn = DriverManager.getConnection("jdbc:postgresql://csce-315-db.engr.tamu.edu/csce315902_12db",
               "csce315902_12user", "team12");
             Statement stmt = conn.createStatement();
             String itemName = addMItem_name_jtext.getText();
             String itemPrice = addMItem_price_jtext.getText();
             String maxIDstmt = "SELECT max(menuid) FROM menu";
             ResultSet maxID = stmt.executeQuery(maxIDstmt);
             maxID.next();
             String maxID2 = maxID.getString(1);
             maxID2 = String.valueOf(Integer.valueOf(maxID2) + 1);
             maxID.close();
             Statement statement = conn.createStatement();
             String SQLstatement = "INSERT INTO menu VALUES("+maxID2+",'"+itemName+"',"+itemPrice+");";
             int result = stmt.executeUpdate(SQLstatement);

        } catch (Exception e) {
          e.printStackTrace();
          System.err.println(e.getClass().getName()+": "+e.getMessage());
          System.exit(0);
        } 
    }
