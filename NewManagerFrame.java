    private void sale_date_nextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sale_date_nextActionPerformed
        // TODO add your handling code here:
        sales_date_back.setEnabled(true);

        try {
            Date curr = new Date();
            Date date = sales_date_chooser.getDate();
            Calendar d = Calendar.getInstance();
            d.setTime(curr);
            int curr_day = d.get(Calendar.DATE);
            int curr_month = d.get(Calendar.MONTH);
            
            if (date.getDay() == curr_day && date.getMonth() == curr_month) {
                sale_date_next.setEnabled(false);
            }

            Calendar c = Calendar.getInstance();
            c.setTime(date);
            c.add(Calendar.DATE, 1);
            date = c.getTime();
            sales_date_chooser.setDate(date);
            
        } catch (Exception e) {
            e.printStackTrace();
            System.err.println(e.getClass().getName()+": "+e.getMessage());
            System.exit(0);
        }
        
        
    }//GEN-LAST:event_sale_date_nextActionPerformed

    private void sales_date_backActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sales_date_backActionPerformed
        // TODO add your handling code here:
        sale_date_next.setEnabled(true);

        try {
        String date_lower = "2022-02-01"; //lower bound date
        java.util.Date date2 = new SimpleDateFormat("yyyy-MM-dd").parse(date_lower);
        Date date = sales_date_chooser.getDate(); //date of jDaychooser box
        Calendar d = Calendar.getInstance();
        d.setTime(date);
        int main_date = d.get(Calendar.DATE); //date of jDaychooser box
        
        Calendar e = Calendar.getInstance(); 
        e.setTime(date2); //lower bound date
        int curr_day = e.get(Calendar.DATE);
        int curr_month = e.get(Calendar.MONTH);
            
        if (main_date == (curr_day+1) && date.getMonth() == curr_month) {
            sales_date_back.setEnabled(false);
        }
        
        Calendar c = Calendar.getInstance();
        c.setTime(date);
        c.add(Calendar.DATE, -1);
        date = c.getTime();
        
        sales_date_chooser.setDate(date);
        } catch (Exception e) {
            e.printStackTrace();
            System.err.println(e.getClass().getName()+": "+e.getMessage());
            System.exit(0);
        }
    }//GEN-LAST:event_sales_date_backActionPerformed

